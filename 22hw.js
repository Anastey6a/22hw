function pow(num, degree) {
    // Якщо ступінь дорівнює 0, то будь-яке число в ступені 0 дорівнює 1
    if (degree === 0) {
        return 1;
    }
    // Якщо ступінь дорівнює 1, то результат просто дорівнює самому числу
    if (degree === 1) {
        return num;
    }
    // Якщо ступінь від'ємний, ми повернемо обернене значення з тим же ступенем
    if (degree < 0) {
        return 1 / pow(num, -degree);
    }
    // Рекурсивний випадок: зводимо число num у ступінь degree
    return num * pow(num, degree - 1);
}

// Приклад використання:
console.log(pow(2, 3)); // Виведе 8 (2^3 = 8)
console.log(pow(5, -2)); // Виведе 0.04 (5^(-2) = 1/25)
